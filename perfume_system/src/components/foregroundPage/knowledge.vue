import axios from "../../Api/api";

export default {
  data() {
    return {
      classify: '',
      material1: '',
      material2: ''
    }
  },
  created() {
    this.getData()
  },
  methods: {
    getData() {
      // 获取所有分类
      axios.getAllclassify(res => {
        if (res.code == 'success') {
          var data = res.data
          this.classify = data.filter(function(item) {
            return item.flavour !== '' || item.odortype !== '' || item.concentration !== '' || item.rank !== ''
          })
        }
      })
      // 获取所有原料1
      axios.getAllmaterial(res => {
        if (res.code == 'success') {
          var data = res.data
          this.material1 = data.filter(function(item) {
            return item.animal !== ''
          })
        }
      })
      // 获取所有原料2
      axios.getAllmaterial2(res => {
        if (res.code == "success") {
          var data = res.data
          this.material2 = data.filter(function(item) {
            return item.plant !== ''
          })
        }
      });
    },
  }
}
